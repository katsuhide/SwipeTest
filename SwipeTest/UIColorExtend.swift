import Foundation
import UIKit

extension UIColor {
    class func zepTealColor() -> UIColor {
        return UIColor(red: 0.0, green: 150.0 / 255.0, blue: 136.0 / 255.0, alpha: 1.0)
    }
    
    class func zepBlackTestColor() -> UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
}
